package br.com.live.model;

public class CapacidadeEmMinutosMes {

	private int mes;
	private int ano;
	private double qtdeMinutos;
	
	public CapacidadeEmMinutosMes() {}
	
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public double getQtdeMinutos() {
		return qtdeMinutos;
	}
	public void setQtdeMinutos(double qtdeMinutos) {
		this.qtdeMinutos = qtdeMinutos;
	}	
}
