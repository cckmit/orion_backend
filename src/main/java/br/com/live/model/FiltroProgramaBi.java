package br.com.live.model;

public class FiltroProgramaBi {
	
	private String id;
	private String descricao;
	private String areaModulo;
	private int atividade;
	private String ferramenta;
	private String frequencia;
	private String planilha;
	private String extrator;
	private String help;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getAreaModulo() {
		return areaModulo;
	}
	public void setAreaModulo(String areaModulo) {
		this.areaModulo = areaModulo;
	}
	public int getAtividade() {
		return atividade;
	}
	public void setAtividade(int atividade) {
		this.atividade = atividade;
	}
	public String getFerramenta() {
		return ferramenta;
	}
	public void setFerramenta(String ferramenta) {
		this.ferramenta = ferramenta;
	}
	public String getFrequencia() {
		return frequencia;
	}
	public void setFrequencia(String frequencia) {
		this.frequencia = frequencia;
	}
	public String getPlanilha() {
		return planilha;
	}
	public void setPlanilha(String planilha) {
		this.planilha = planilha;
	}
	public String getExtrator() {
		return extrator;
	}
	public void setExtrator(String extrator) {
		this.extrator = extrator;
	}
	public String getHelp() {
		return help;
	}
	public void setHelp(String help) {
		this.help = help;
	}
}
